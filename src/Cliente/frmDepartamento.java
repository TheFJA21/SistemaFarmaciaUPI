    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        boolean v= false;
        if(txtCodigo.getText().isEmpty()||txtNombre.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "No se pueden dejar espacios en blanco");
        }
        else{
            int codi= Integer.parseInt(txtCodigo.getText());
            for(int i=0; i<op.array_Departamento.size();i++){
                
                Departamento dep = (Departamento)op.array_Departamento.get(i);
                if(op.array_Departamento.get(i).getCodigo()==codi){
                    
                    v=true;
                }
                
            }
            if(v==true){
                
                JOptionPane.showMessageDialog(null, "La cedula ya existe, ingrese otra");
            }
            else{
                
                int codigo= Integer.parseInt(txtCodigo.getText());
                String nombre= String.valueOf(txtNombre.getText());
                
                op.agregar(nombre, codigo);
                
            }
        }
        txtNombre.setText("");
        txtCodigo.setText("");
        
        txtCodigo.setEnabled(false);
        txtNombre.setEnabled(false);
        
        btnOK.setEnabled(false);
        btnConsultar.setEnabled(true);
        btnGuardar.setEnabled(false);
        btnNuevo.setEnabled(true);
        btnModificar.setEnabled(false);
        btnEliminar.setEnabled(false);
        btnSalir.setEnabled(true);
        
    }//GEN-LAST:event_btnGuardarActionPerformed

