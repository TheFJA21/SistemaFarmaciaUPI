    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        boolean v= false;
        
        if(txtCodigo.getText().isEmpty()){
            JOptionPane.showMessageDialog(null, "Se debe digitar un codigo de farmaco");
        }
        else{
            int codi= Integer.parseInt(txtCodigo.getText());
            for(int i=0; i<op.array_Departamento.size();i++){
                
                Departamento dep = (Departamento)op.array_Departamento.get(i);
                if(op.array_Departamento.get(i).getCodigo()==codi){
                    
                    v=true;
                }
                
            }
            if(v==true){
                
                for(int i=0; i<op.array_Departamento.size();i++){
                    
                    Departamento dep = (Departamento)op.array_Departamento.get(i);
                    if(op.array_Departamento.get(i).getCodigo()==codi){
                        
                        txtNombre.setText(String.valueOf(dep.getNombre()));
                    }
            }
            }
            }
        btnOK.setEnabled(false);
        btnConsultar.setEnabled(false);
        btnGuardar.setEnabled(false);
        btnNuevo.setEnabled(true);
        btnModificar.setEnabled(true);
        btnEliminar.setEnabled(true);
        btnSalir.setEnabled(true);
    
    }//GEN-LAST:event_btnOKActionPerformed

    private void txtCodigoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtCodigoKeyTyped
        char c= evt.getKeyChar();
        if(Character.isLetter(c)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "No se pueden digitar letras");
        }
    }//GEN-LAST:event_txtCodigoKeyTyped

    private void txtNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNombreKeyTyped
        char c= evt.getKeyChar();
        if(Character.isDigit(c)){
            evt.consume();
            JOptionPane.showMessageDialog(null, "No se pueden digitar numeros");
        }
    }//GEN-LAST:event_txtNombreKeyTyped

